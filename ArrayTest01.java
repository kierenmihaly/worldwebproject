public class ArrayTest01{

		public static void main(String args[]) {

	int[] intArray = new int[9];

	/*
	intArray[0]=1;
	intArray[1]=2;
	intArray[2]=3;
	intArray[3]=4;
	intArray[4]=5;
	intArray[5]=6;
	intArray[6]=7;
	intArray[7]=8;
	intArray[8]=9;
	*/


	//배열의 index를 이용 각각의 value에 접근 
	for( int j = 0; j < 9; j++){
		System.out.println("intArray["+j+"] 값은 : "+intArray[j]);
	}

	//각각의 값을 이용하여 5단을 출력 intArray.length
	//Operator 와 length keyword 사용 
	System.out.println("==================");
	for( int k = 0; k<intArray.length; k++) {
		System.out.println(" 5 X " +intArray[k]+"="+5*intArray[k]);
	}

	//아래의 출력 내용을 이해하자 (primitive int i =0;과 비교 이해하면됨)
	int [] j = intArray;
	System.out.println("==================");
	System.out.println("j[0]의 값은 :" +j[0]);
	System.out.println("j[5]의 값은 :" +j[5]);

	j[5]=1234;
	System.out.println("j[5]==>"+j[5]);


		}//main
}//class